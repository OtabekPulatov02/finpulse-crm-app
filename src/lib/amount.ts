/* Расставляет пробелы-разделители тысяч в числах внутри произвольного
   текста — "1000000" -> "1 000 000", как для сумм в названиях задач,
   напоминаний и т.д. Форматируется на blur (не на каждое нажатие
   клавиши), чтобы не прыгал курсор во время набора.

   Специально ограничено 6–12 цифрами подряд, чтобы не портить короткие
   числа (годы, даты, счётчики) и не путать с длинными телефонными
   номерами — под реальные суммы (от ~100 000 сум) этого достаточно. */
export function formatSumsInText(text: string): string {
  if (!text) return text;
  return text.replace(/(?<![+№\d])\d{6,12}(?!\d)/g, (m) =>
    m.replace(/\B(?=(\d{3})+(?!\d))/g, " ")
  );
}
